export function generateAiResponse(questId: string): string {
  return AI_RESPONSES[questId] || '분석 중... AI가 응답을 생성하고 있습니다.'
}

export function createBattleSession(quest: QuestConfig, roomId: string): BattleSession {
  const aiSteps: ReadonlyArray<AiStep> = quest.aiSteps.map((step, i) => ({
    id: `step-${i}`,
    label: step.label,
    description: step.description,
    status: 'pending' as const,
    output: null,
  }))

  return {
    id: crypto.randomUUID(),
    questId: quest.id,
    roomId,
    status: 'preparing',
    startedAt: null,
    endedAt: null,
    timeLimit: quest.timeLimit,
    timeRemaining: quest.timeLimit,
    humanSubmission: '',
    aiSubmission: '',
    aiProgress: 0,
    aiSteps,
    scores: null,
    winner: null,
  }
}

export function evaluateSubmissions(
  humanText: string,
  _aiText: string,
  questId: string,
): BattleScoreBreakdown {
  const len = humanText.trim().length
  const baseQuality = Math.min(95, 40 + Math.floor(len / 15))
  const baseCreativity = Math.min(95, 45 + Math.floor(len / 20))

  const questBias: Record<string, number> = {
    q1: 2, q2: 5, q3: 3, q4: 8, q5: 6,
    q6: 7, q7: 4, q8: 10, q9: 1, q10: 9,
    q11: 8, q12: 7,
  }
  const bias = questBias[questId] || 5

  const humanScores = {
    quality: Math.min(98, baseQuality + Math.floor(Math.random() * 10)),
    creativity: Math.min(98, baseCreativity + Math.floor(Math.random() * 12) + bias),
    execution: Math.min(95, 50 + Math.floor(len / 12) + Math.floor(Math.random() * 8)),
    efficiency: Math.min(90, 40 + Math.floor(Math.random() * 25)),
  }

  const aiScores = {
    quality: 70 + Math.floor(Math.random() * 15),
    creativity: 55 + Math.floor(Math.random() * 15),
    execution: 75 + Math.floor(Math.random() * 15),
    efficiency: 82 + Math.floor(Math.random() * 13),
  }

  const totalHuman = humanScores.quality + humanScores.creativity + humanScores.execution + humanScores.efficiency
  const totalAi = aiScores.quality + aiScores.creativity + aiScores.execution + aiScores.efficiency

  return {
    quality: { human: humanScores.quality, ai: aiScores.quality },
    creativity: { human: humanScores.creativity, ai: aiScores.creativity },
    execution: { human: humanScores.execution, ai: aiScores.execution },
    efficiency: { human: humanScores.efficiency, ai: aiScores.efficiency },
    totalHuman,
    totalAi,
  }
}
